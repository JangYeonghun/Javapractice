public class ex2_10 {
    public static void main(String[] args) {
        int x = 3;
        int y = 4;
        System.out.println((x == y) + " "); // f
        System.out.println((x != y) + " "); // t
        System.out.println((x > y) + " "); // f
        System.out.println((x < y) + " "); // t
        System.out.println((x <= y) + " "); // t
    }
}
